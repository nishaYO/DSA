public class DataTypes {
    public static void main(String[] args) {
        int num = 2;
        double decimalNum = 3.4;
        char character = '1';
        char character2 = 'q';
        boolean bool = true;
        String text = "hello world";
        System.out.println("Int num is " + num);
        System.out.println("Double decimalNum is " + decimalNum);
        System.out.println("char character is " + character);
        System.out.println("char character2 is " + character2);
        System.out.println("boolean bool is " + bool);
        System.out.println("String text is "+ text);
    }
}
